func TestDecodeInvalid(t *testing.T) {
	testCases := []struct {
		title string
		data  []byte
	}{
		{
			title: "empty",
		},
		{
			title: "zero length",
			data:  []byte{0},
		},
		{
			title: "first oversized length",
			data:  []byte{255, 0},
		},
		{
			title: "second oversized length",
			data:  []byte{1, 255, 0},
		},
	}
	for _, tc := range testCases {
		t.Run(tc.title, func(t *testing.T) {
			if got := Decode(tc.data); got != nil {
				t.Errorf("got %v, want nil", got)
			}
		})
	}
}

func TestRandom(t *testing.T) {
	f := func(p []byte) bool {
		p = append(p, 0) // zero-terminate input
		return bytes.Equal(Decode(Encode(p)), p)
	}
	if err := quick.Check(f, nil); err != nil {
		t.Error(err)
	}
}
